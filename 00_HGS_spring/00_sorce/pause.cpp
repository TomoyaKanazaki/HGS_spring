#include "pause.h"
#include "input.h"
#include "debugproc.h"

//==========================================
//  グローバル変数宣言
//==========================================
bool g_bPause;

//==========================================
//  初期化処理
//==========================================
void InitPause()
{
	//変数の初期化
	g_bPause = false;
}

//==========================================
//  終了処理
//==========================================
void UninitPause()
{
	
}

//==========================================
//  更新処理
//==========================================
void UpdatePause()
{
	if (GetKeyboardTrigger(DIK_P))
	{
		g_bPause = !g_bPause;
	}
}

//==========================================
//  描画処理
//==========================================
void DrawPause()
{
	//デバッグ表示
	if (g_bPause)
	{
		PrintDebugProc("ポーズ中");
	}

	//ポーズ中の場合
	if (g_bPause)
	{

	}
}

//==========================================
//  ポーズ状態の取得
//==========================================
bool GetPause()
{
	return g_bPause;
}
